// boolean return value true  with string or not

let boolean = 10 > 9

console.log(boolean)   // true

console.log(typeof(boolean))  // boolean

console.log(boolean.valueOf())  // true

console.log(boolean.toString())  // true

let val = 10 < 9

console.log(val.valueOf())   // false

console.log(val.toString())  // false

