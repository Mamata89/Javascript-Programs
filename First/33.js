//What is the length of STRING SPLIT METHOD

let name="Js startup"

console.log(name)         //  Js startup
 
console.log(name.length)  // 10


let splitstring = name.split()

console.log(splitstring)   ['Js startup']

console.log(splitstring.length)


let splitstring1 = name.split('')

console.log(splitstring1)           // ['J', 's', ' ', 's', 't', 'a', 'r', 't', 'u', 'p']


let splitstring2 = name.split(' ')

console.log(splitstring2)           // ['Js', 'startup']

let splitstring3 = name.split(',')

console.log(splitstring3)           // ['Js startup']

